for (const [loc, obj] of Object.entries(extras)) {
  const block = Object.entries(obj)
    .map(([k, v]) => `    ${k}: ${JSON.stringify(v)},`)
    .join("\n")
  const re = new RegExp(`(  ${loc}: \\{[\\s\\S]*?)(    language: )`)
  if (!re.test(s)) {
    console.error("no match", loc)
    process.exit(1)
  }
  s = s.replace(re, (_, a, b) => a + block + "\n" + b)
}

